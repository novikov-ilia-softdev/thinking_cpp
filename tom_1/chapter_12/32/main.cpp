#include <iostream>

int main()
{
	double d1 = 3.14;
	double d2 = 2.71;
	std::cout <<  "d1 = " << d1 << std::endl;
	std::cout <<  "d2 = " << d2 << std::endl;
	std::cout <<  "d1 + d2 = " << d1 + d2 << std::endl;
	std::cout <<  "d1 - d2 = " << d1 - d2 << std::endl;
	std::cout <<  "d1 * d2 = " << d1 * d2 << std::endl;
	std::cout <<  "d1 / d2 = " << d1 / d2 << std::endl;
}
